//  City Names: Write a function called city_country() that takes in the name of a city and its country. The function should return a string formatted

function city_country(city: string, country: string):string{
    return city + ", " + country;
};

console.log(city_country('Quetta', 'Pakistan'));
console.log(city_country('Kolkata', 'India'));
console.log(city_country('Tehran', 'Iran'));
